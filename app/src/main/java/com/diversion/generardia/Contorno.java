package com.diversion.generardia;

public class Contorno {

    public int codContorno;
    public String descContorno;

    public Contorno() {
    }

    public int getCodContorno() {
        return codContorno;
    }

    public void setCodContorno(int codContorno) {
        this.codContorno = codContorno;
    }

    public String getDescContorno() {
        return descContorno;
    }

    public void setDescContorno(String descContorno) {
        this.descContorno = descContorno;
    }
}
