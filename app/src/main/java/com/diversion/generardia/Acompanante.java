package com.diversion.generardia;

public class Acompanante {

    public int codAcompanante;
    public String descAcompanante;

    public Acompanante() {
    }

    public int getCodAcompanante() {
        return codAcompanante;
    }

    public void setCodAcompanante(int codAcompanante) {
        this.codAcompanante = codAcompanante;
    }

    public String getDescAcompanante() {
        return descAcompanante;
    }

    public void setDescAcompanante(String descAcompanante) {
        this.descAcompanante = descAcompanante;
    }
}
