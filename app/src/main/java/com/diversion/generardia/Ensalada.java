package com.diversion.generardia;

public class Ensalada {

    public int codEnsalada;
    public String descEnsalada;

    public Ensalada() {
    }

    public int getCodEnsalada() {
        return codEnsalada;
    }

    public void setCodEnsalada(int codEnsalada) {
        this.codEnsalada = codEnsalada;
    }

    public String getDescEnsalada() {
        return descEnsalada;
    }

    public void setDescEnsalada(String descEnsalada) {
        this.descEnsalada = descEnsalada;
    }
}
